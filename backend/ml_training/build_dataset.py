def build_dataset(output_path: str = "dataset/smishing_ko.csv"):
    """데이터셋 CSV 파일 생성"""
    os.makedirs(os.path.dirname(output_path), exist_ok=True)

    rows = []
    # 스미싱 샘플 추가 (label=1)
    for text in SMISHING_SAMPLES:
        rows.append({"label": 1, "text": text})

    # 정상 샘플 추가 (label=0)
    for text in NORMAL_SAMPLES:
        rows.append({"label": 0, "text": text})

    # 섞기 (학습 시 편향 방지)
    random.seed(42)
    random.shuffle(rows)

    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["label", "text"])
        writer.writeheader()
        writer.writerows(rows)

    smishing_count = sum(1 for r in rows if r["label"] == 1)
    normal_count = sum(1 for r in rows if r["label"] == 0)
    print(f"✅ 데이터셋 생성 완료: {output_path}")
    print(f"   스미싱: {smishing_count}건 | 정상: {normal_count}건 | 합계: {len(rows)}건")
    return output_path


